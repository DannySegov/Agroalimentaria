    private void generarTable(){
        
        ControladorProductoCongelado controlador = new ControladorProductoCongelado();
        
        List<ProductoCongelado> listaProductos = controlador.mostrarTodoProductosF();
        
        DefaultTableModel modeloTabla = new DefaultTableModel(new String[]{"id", "Clave", "Descripci贸n", "Cantidad", "No. Lote", "Fecha Cad.",
        "Temperatura Recom.", "Tipo de Congelaci贸n"}, 0);
        Object[] fila = new Object[modeloTabla.getColumnCount()];
        
        this.tablaProductosC.setModel(modeloTabla);
        
        if (listaProductos != null){
            
            if (!listaProductos.isEmpty()) {
                
                for (int i = 0; i<listaProductos.size(); i++){
                    
                    fila[0] = (i+1);
                    fila[1] = listaProductos.get(i).getClave();
                    fila[2] = listaProductos.get(i).getDescripcion();
                    fila[3] = listaProductos.get(i).getCantidad();
                    fila[4] = listaProductos.get(i).getNumeroLote();
                    fila[5] = listaProductos.get(i).getFechaCaducidad();
                    fila[6] = listaProductos.get(i).getTemperaturaRecomendada().replace("/", " ");
                    fila[7] = listaProductos.get(i).getTipoCongelacion();
                    
                    modeloTabla.addRow(fila);
                }
                
            }else{
                JOptionPane.showMessageDialog(null, "Lista vacia.\nNo se han encontrado registros.", "Lista productos congelados", 3);
            }
            
        }else{
            JOptionPane.showMessageDialog(null, "Lista vacia.\nNo se han encontrado registros.", "Lista productos congelados", 3);
        }
    }

