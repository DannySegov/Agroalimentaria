    private void agregarCantidad() {
        for (int i = 1; i <= 100; i++) {
            this.cbxCantidadPCM.addItem(Integer.toString(i));
        }
    }

    private void agregarNumGrados() {
        for (int i = -10; i <= 10; i++) {
            this.cbxNumeroGPCM.addItem(Integer.toString(i));
        }

        this.cbxNumeroGPCM.setSelectedItem("0");
    }

    private void agregarFecha() {
        for (int i = 1; i <= 31; i++) {
            this.cbxDiaPCM.addItem(Integer.toString(i));
        }
        for (int i = 2021; i < 2050; i++) {
            this.cbxAnioPCM.addItem(Integer.toString(i));
        }
    }

    private void limpiarObjetos() {
        String[] items = {"Selecciona", "contacto_directo", "por_aire", "criogénico"};
        this.tfClavePCM.setText("");
        this.cbxdescripcionPCM.setSelectedIndex(0);
        this.cbxCantidadPCM.setSelectedIndex(0);
        this.tfNoLotePCM.setText("");
        this.cbxDiaPCM.setSelectedIndex(0);
        this.cbxMesPCM.setSelectedIndex(0);
        this.cbxAnioPCM.setSelectedIndex(0);
        this.cbxNumeroGPCM.setSelectedItem("0");
        this.cbxTipoGPCM.setSelectedIndex(0);
        this.cbxTipoPCM.removeAllItems();
        for (String item : items){
            this.cbxTipoPCM.addItem(item);
        }
    }

