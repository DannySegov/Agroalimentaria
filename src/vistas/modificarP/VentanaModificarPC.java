    private void btnBuscarPCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarPCActionPerformed
        if (!this.tfClavePCM.getText().equals("")) {

            if (this.controlador.validarClave(this.tfClavePCM.getText())) {

                ProductoCongelado producto = this.controlador.buscarProducto(this.tfClavePCM.getText());
                habilitarCampos();

                this.cbxdescripcionPCM.setSelectedItem(producto.getDescripcion());
                this.cbxCantidadPCM.setSelectedIndex(producto.getCantidad());

                String[] fecha = producto.getFechaCaducidad().split("/");
                this.cbxDiaPCM.setSelectedItem(fecha[0]);
                this.cbxMesPCM.setSelectedIndex(Integer.parseInt(fecha[1]));
                this.cbxAnioPCM.setSelectedItem(fecha[2]);

                String[] temperatura = producto.getTemperaturaRecomendada().split("/");
                this.cbxNumeroGPCM.setSelectedItem(temperatura[0]);
                this.cbxTipoGPCM.setSelectedItem(temperatura[1]);

                this.cbxTipoPCM.setSelectedItem(producto.getTipoCongelacion());
                this.tfNoLotePCM.setText(producto.getNumeroLote());

            } else {
                JOptionPane.showMessageDialog(null, "No se encontro ningun producto con esa clave");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Se necesita una clave par abuscar.");
        }
    }//GEN-LAST:event_btnBuscarPCActionPerformed

    private void btnGuardarPCMActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarPCMActionPerformed
        ProductoCongelado nuevoProducto = new ProductoCongelado();
        controlador = new ControladorProductoCongelado();
        if (this.cbxdescripcionPCM.getSelectedIndex() != 0) {

            if (this.cbxCantidadPCM.getSelectedIndex() != 0) {

                if (!this.tfNoLotePCM.getText().equals("")) {

                    if (this.cbxDiaPCM.getSelectedIndex() != 0 || this.cbxMesPCM.getSelectedIndex() != 0 || this.cbxAnioPCM.getSelectedIndex() != 0) {

                        if (this.cbxTipoPCM.getSelectedIndex() != 0) {

                            nuevoProducto.setClave(this.tfClavePCM.getText());
                            nuevoProducto.setCantidad(Integer.parseInt(this.cbxCantidadPCM.getItemAt(this.cbxCantidadPCM.getSelectedIndex())));
                            nuevoProducto.setDescripcion(this.cbxdescripcionPCM.getItemAt(this.cbxdescripcionPCM.getSelectedIndex()));

                            String fecha = this.cbxDiaPCM.getItemAt(this.cbxDiaPCM.getSelectedIndex()) + "/"
                            + this.cbxMesPCM.getSelectedIndex() + "/"
                            + this.cbxAnioPCM.getItemAt(this.cbxAnioPCM.getSelectedIndex());

                            nuevoProducto.setFechaCaducidad(fecha);

                            String temp = this.cbxNumeroGPCM.getItemAt(this.cbxNumeroGPCM.getSelectedIndex()) + "/"
                            + this.cbxTipoGPCM.getItemAt(this.cbxTipoGPCM.getSelectedIndex());

                            nuevoProducto.setTemperaturaRecomendada(temp);

                            nuevoProducto.setTipoCongelacion(this.cbxTipoPCM.getItemAt(this.cbxTipoPCM.getSelectedIndex()));
                            nuevoProducto.setNumeroLote(this.tfNoLotePCM.getText());

                            limpiarObjetos();
                            desactivarCampos();
                            controlador.modificarProducto(nuevoProducto);
                        } else {
                            JOptionPane.showMessageDialog(null, "El producto debe de tener un tipo de alimento.");
                        }

                    } else {
                        JOptionPane.showMessageDialog(null, "Ingresa una fecha valida.");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "El producto debe de tener un número de lote.");
                }

            } else {
                JOptionPane.showMessageDialog(null, "La cantidad del producto debe de ser mayor a cero.");
            }

        } else {
            JOptionPane.showMessageDialog(null, "Es necesario seleccionar una descripcion\ndel producto.");
        }
    }//GEN-LAST:event_btnGuardarPCMActionPerformed

