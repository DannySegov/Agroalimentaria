    private void btnBuscarPFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarPFActionPerformed
        if (!this.tfClavePFM.getText().equals("")) {

            if (this.controlador.validarClave(this.tfClavePFM.getText())) {

                ProductoFresco producto = this.controlador.buscarProducto(this.tfClavePFM.getText());
                habilitarCampos();

                this.cbxdescripcionPFM.setSelectedItem(producto.getDescripcion());
                this.cbxCantidadPFM.setSelectedIndex(producto.getCantidad());

                String[] fecha = producto.getFechaCaducidad().split("/");
                this.cbxDiaPFM.setSelectedItem(fecha[0]);
                this.cbxMesPFM.setSelectedIndex(Integer.parseInt(fecha[1]));
                this.cbxAnioPFM.setSelectedItem(fecha[2]);

                String[] temperatura = producto.getTemperaturaRecomendada().split("/");
                this.cbxNumeroGPFM.setSelectedItem(temperatura[0]);
                this.cbxTipoGPFM.setSelectedItem(temperatura[1]);

                this.cbxTipoPFM.setSelectedItem(producto.getTipoAlimento());
                this.tfNoLotePFM.setText(producto.getNumeroLote());

            } else {
                JOptionPane.showMessageDialog(null, "No se encontro ningun producto con esa clave");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Se necesita una clave par abuscar.");
        }
    }//GEN-LAST:event_btnBuscarPFActionPerformed


