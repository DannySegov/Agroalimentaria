    private void agregarCantidad() {
        for (int i = 1; i <= 100; i++) {
            this.cbxCantidadPFM.addItem(Integer.toString(i));
        }
    }

    private void agregarNumGrados() {
        for (int i = -10; i <= 10; i++) {
            this.cbxNumeroGPFM.addItem(Integer.toString(i));
        }

        this.cbxNumeroGPFM.setSelectedItem("0");
    }

    private void agregarFecha() {
        for (int i = 1; i <= 31; i++) {
            this.cbxDiaPFM.addItem(Integer.toString(i));
        }
        for (int i = 2021; i < 2050; i++) {
            this.cbxAnioPFM.addItem(Integer.toString(i));
        }
    }

