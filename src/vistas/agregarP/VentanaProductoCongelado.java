    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        ProductoCongelado nuevoProducto = new ProductoCongelado();
        ControladorProductoCongelado controlador = new ControladorProductoCongelado();
        
        if (this.cbxdescripcionPC.getSelectedIndex() != 0) {

            if (this.cbxCantidadPC.getSelectedIndex() != 0) {

                if (!this.tfNoLotePC.getText().equals("")) {

                    if (this.cbxDiaPC.getSelectedIndex() != 0 || this.cbxMesPC.getSelectedIndex() != 0 || this.cbxAnioPC.getSelectedIndex() != 0) {

                        if (this.cbxTipoPC.getSelectedIndex() != 0) {

                            if (!controlador.validarClave(this.tfClavePC.getText())) {

                                nuevoProducto.setClave(this.tfClavePC.getText());
                                nuevoProducto.setCantidad(Integer.parseInt(this.cbxCantidadPC.getItemAt(this.cbxCantidadPC.getSelectedIndex())));
                                nuevoProducto.setDescripcion(this.cbxdescripcionPC.getItemAt(this.cbxdescripcionPC.getSelectedIndex()));

                                String fecha = this.cbxDiaPC.getItemAt(this.cbxDiaPC.getSelectedIndex()) + "/"
                                        + this.cbxMesPC.getSelectedIndex() + "/"
                                        + this.cbxAnioPC.getItemAt(this.cbxAnioPC.getSelectedIndex());

                                nuevoProducto.setFechaCaducidad(fecha);

                                String temp = this.cbxNumeroGPC.getItemAt(this.cbxNumeroGPC.getSelectedIndex()) + "/"
                                        + this.cbxTipoGPC.getItemAt(this.cbxTipoGPC.getSelectedIndex());

                                nuevoProducto.setTemperaturaRecomendada(temp);

                                nuevoProducto.setTipoCongelacion(this.cbxTipoPC.getItemAt(this.cbxTipoPC.getSelectedIndex()));
                                nuevoProducto.setNumeroLote(this.tfNoLotePC.getText());

                                limpiarObjetos();
                                controlador.agregarProducto(nuevoProducto);
                            } else {
                                JOptionPane.showMessageDialog(null, "La clave " + this.tfClavePC.getText() + "\nYa se encuentra registrada", "Agregar producto fresco", 2);
                            }

                        } else {
                            JOptionPane.showMessageDialog(null, "El producto debe de tener un tipo de alimento.");
                        }

                    } else {
                        JOptionPane.showMessageDialog(null, "Ingresa una fecha valida.");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "El producto debe de tener un número de lote.");
                }

            } else {
                JOptionPane.showMessageDialog(null, "La cantidad del producto debe de ser mayor a cero.");
            }

        } else {
            JOptionPane.showMessageDialog(null, "Es necesario seleccionar una descripcion\ndel producto.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

