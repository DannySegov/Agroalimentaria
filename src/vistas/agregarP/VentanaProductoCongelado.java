    private void agregarCantidad() {
        for (int i = 1; i <= 100; i++) {
            this.cbxCantidadPC.addItem(Integer.toString(i));
        }
    }

    private void agregarNumGrados() {
        for (int i = -10; i <= 10; i++) {
            this.cbxNumeroGPC.addItem(Integer.toString(i));
        }

        this.cbxNumeroGPC.setSelectedItem("0");
    }

    private void agregarFecha() {
        for (int i = 1; i <= 31; i++) {
            this.cbxDiaPC.addItem(Integer.toString(i));
        }
        for (int i = 2021; i < 2050; i++) {
            this.cbxAnioPC.addItem(Integer.toString(i));
        }
    }

    private void limpiarObjetos() {
        String[] items = {"Selecciona", "contacto_directo", "por_aire", "criogénico"};
        this.tfClavePC.setText("");
        this.cbxdescripcionPC.setSelectedIndex(0);
        this.cbxCantidadPC.setSelectedIndex(0);
        this.tfNoLotePC.setText("");
        this.cbxDiaPC.setSelectedIndex(0);
        this.cbxMesPC.setSelectedIndex(0);
        this.cbxAnioPC.setSelectedIndex(0);
        this.cbxNumeroGPC.setSelectedItem("0");
        this.cbxTipoGPC.setSelectedIndex(0);
        this.cbxTipoPC.removeAllItems();
        for (String item : items){
            this.cbxTipoPC.addItem(item);
        }
    }

